%#36 MAX'
dy(36)=-y(36);
%#37 MYC'
dy(37)=-y(37)+0.37*y(33)^3/(0.5^3+y(33)^3)+0.37*y(14)^3/(0.5^3+y(14)^3)+0.37*y(4)^3/(0.5^3+y(4)^3)+0.5*0.5^3/(0.5^3+y(38)^3);
%#38 TCF3'
dy(38)=(-1)*y(38)+0.37*y(4)^3/(0.5^3+y(4)^3);
%#39 MycMax'
dy(39)=(-1)*y(39)+0.37*y(36)^3/(0.5^3+y(36)^3)+0.37*y(37)^3/(0.5^3+y(37)^3);
%#40 MYCSP1'
dy(40)=(-1)*y(40)+0.37*y(23)^3/(0.5^3+y(23)^3)+0.37*y(37)^3/(0.5^3+y(37)^3);
%#41 NFYA'
dy(41)=-y(41)+0.37*y(4)^3/(0.5^3+y(4)^3)+0.37*y(47)^3/(0.5^3+y(47)^3);
%#42 SALL4'
dy(42)=-y(42)+0.37*y(32)^3/(0.5^3+y(32)^3)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.37*y(47)^3/(0.5^3+y(47)^3);
%#43 ZNF206'
dy(43)=-y(43)+0.37*y(44)^3/(0.5^3+y(44)^3);
%#44 NANOG'
dy(44)=-y(44)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.37*y(46)^3/(0.5^3+y(46)^3)+0.37*y(2)^3/(0.5^3+y(2)^3)+0.37*y(48)^3/(0.5^3+y(48)^3)+0.37*y(49)^3/(0.5^3+y(49)^3)+0.5*0.5^3/(0.5^3+y(3)^3);
%#45 OCT4'
dy(45)=-y(45)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.37*y(46)^3/(0.5^3+y(46)^3)+0.5*0.5^3/(0.5^3+y(1)^3);
