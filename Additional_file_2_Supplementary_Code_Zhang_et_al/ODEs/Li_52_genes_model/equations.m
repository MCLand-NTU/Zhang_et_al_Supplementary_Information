% #9 FOXA1'
dy(9)=-y(9)+0.37*y(9)^3/(0.5^3+y(9)^3)+0.37*y(50)^3/(0.5^3+y(50)^3)+0.5*0.5^3/(0.5^3+y(8)^3);
% #10 AFP'
dy(10)=-y(10)+0.37*y(9)^3/(0.5^3+y(9)^3)+0.37*y(6)^3/(0.5^3+y(6)^3);
% #11 SUMO1'
dy(11)=-y(11)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.37*y(4)^3/(0.5^3+y(4)^3);
% #12 GATA4'
dy(12)=-y(12)+0.37*y(11)^3/(0.5^3+y(11)^3)+0.37*y(13)^3/(0.5^3+y(13)^3)+0.5*0.5^3/(0.5^3+y(44)^3)+0.5*0.5^3/(0.5^3+y(45)^3);
% #13 PIAS1'
dy(13)=-y(13)+0.37*y(13)^3/(0.5^3+y(13)^3)+0.37*y(4)^3/(0.5^3+y(4)^3);
% #14 PRDM14'
dy(14)=-y(14)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.37*y(45)^3/(0.5^3+y(45)^3)+0.37*y(47)^3/(0.5^3+y(47)^3);
% #15 LMCD1'
dy(15)=-y(15)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.5*0.5^3/(0.5^3+y(22)^3);
% #16 T'
dy(16)=-y(16)+0.5*0.5^3/(0.5^3+y(45)^3)+0.5*0.5^3/(0.5^3+y(44)^3)+0.5*0.5^3/(0.5^3+y(14)^3)+0.5*0.5^3/(0.5^3+y(4)^3);
% #17 CSH1'
dy(17)=-y(17)+0.5*0.5^3/(0.5^3+y(14)^3);
% #18 GATA2'
dy(18)=-y(18)+0.5*0.5^3/(0.5^3+y(47)^3)+0.5*0.5^3/(0.5^3+y(44)^3)+0.5*0.5^3/(0.5^3+y(19)^3)+0.5*0.5^3/(0.5^3+y(11)^3);
% #19 PIASy'
dy(19)=-y(19)+0.37*y(13)^3/(0.5^3+y(13)^3);
% #20 GATA3'
dy(20)=-y(20)+0.5*0.5^3/(0.5^3+y(44)^3);
% #21 hCGa'
dy(21)=-y(21)+0.37*y(18)^3/(0.5^3+y(18)^3)+0.37*y(20)^3/(0.5^3+y(20)^3)+0.37*y(22)^3/(0.5^3+y(22)^3)+0.5*0.5^3/(0.5^3+y(45)^3);
