% #26 BMP2K'
dy(26)=-y(26)+0.37*y(28)^3/(0.5^3+y(28)^3);
% #27 BMP2'
dy(27)=(-1)*y(27)+0.37*y(26)^3/(0.5^3+y(26)^3)+0.5*0.5^3/(0.5^3+y(47)^3)+0.5*0.5^3/(0.5^3+y(44)^3);
% #28 HNF4A'
dy(28)=-y(28)+0.37*y(3)^3/(0.5^3+y(3)^3)+0.37*y(29)^3/(0.5^3+y(29)^3);
% #29 bCAT'
dy(29)=-y(29);
%#30 CEBP'
