%#47 SOX2'
dy(47)=-y(47)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.37*y(46)^3/(0.5^3+y(46)^3);
%#48 ZIC3'
dy(48)=-y(48)+0.37*y(44)^3/(0.5^3+y(44)^3)+0.37*y(47)^3/(0.5^3+y(47)^3);
